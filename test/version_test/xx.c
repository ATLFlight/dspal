static __inline __uint32_t
__bswap32_var(__uint32_t v)
{
 __uint32_t t1;

 __asm volatile("eor %1, %0, %0, ror #16\n"
    "bic %1, %1, #0x00ff0000\n"
    "mov %0, %0, ror #8\n"
    "eor %0, %0, %1, lsr #8\n"
    : "+r"(v), "=r"(t1));

 return (v);
}

